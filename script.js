// Global Map Variable
let tacticalMap = null;
let marker = null;

// Initialize Map on Load
document.addEventListener('DOMContentLoaded', () => {
    initMap(0, 0, 1); // Default center
});

function initMap(lat, lon, zoomLevel) {
    if (!tacticalMap) {
        tacticalMap = L.map('map', {
            zoomControl: false,
            attributionControl: false
        }).setView([lat, lon], zoomLevel);

        // Cyber-themed map tiles (using CartoDB Dark Matter as a base, CSS inverts/filters it)
        L.tileLayer('https://{s}.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}{r}.png', {
            maxZoom: 19
        }).addTo(tacticalMap);

        // Custom cyber marker
        const cyberIcon = L.divIcon({
            className: 'cyber-marker',
            html: '<div style="color: var(--danger-color); font-size: 24px; text-shadow: 0 0 10px var(--danger-color);"><i class="fa-solid fa-crosshairs fa-spin-pulse"></i></div>',
            iconSize: [24, 24],
            iconAnchor: [12, 12]
        });

        marker = L.marker([lat, lon], { icon: cyberIcon }).addTo(tacticalMap);
    } else {
        tacticalMap.setView([lat, lon], zoomLevel);
        marker.setLatLng([lat, lon]);
    }
}

